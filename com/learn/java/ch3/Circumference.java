package com.learn.java.ch3;

public class Circumference {
	public static void main(String args[])
	{
		float radius = 3,length = 2,width = 4;
		System.out.println("circle : "+ (2*3.142*radius));
		System.out.println("square : "+ (2*(length+width)));
		System.out.println("rectangle : "+ 4*length);
	}
}
